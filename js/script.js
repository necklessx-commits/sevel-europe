function parseJsonOrNull(raw) {
  if (!raw) return null;
  try {
    return JSON.parse(raw);
  } catch (_) {
    return null;
  }
}

// Submit payload via hidden form targeting an invisible iframe (CORS-free fallback)
function submitWebhookForm(payload, webhookUrl, webhookToken) {
  try {
    const iframeName = "sevelWebhookFrame";
    let iframe = document.querySelector('iframe[name="' + iframeName + '"]');
    if (!iframe) {
      iframe = document.createElement('iframe');
      iframe.name = iframeName;
      iframe.style.display = 'none';
      document.body.appendChild(iframe);
    }

    const form = document.createElement('form');
    form.method = 'POST';
    form.action = webhookUrl;
    form.target = iframeName;
    form.style.display = 'none';

    const addInput = (name, value) => {
      const i = document.createElement('input');
      i.type = 'hidden';
      i.name = name;
      i.value = value || '';
      form.appendChild(i);
    };

    addInput('token', webhookToken);
    ['name', 'email', 'phone', 'subject', 'message', 'status', 'submittedAt'].forEach((k) => addInput(k, payload[k] || ''));

    document.body.appendChild(form);
    form.submit();
    setTimeout(() => {
      form.remove();
    }, 5000);
  } catch (err) {
    console.warn('submitWebhookForm failed', err);
  }
}

function getStoredArray(key) {
  try {
    const raw = localStorage.getItem(key);
    const parsed = raw ? parseJsonOrNull(raw) : [];
    return Array.isArray(parsed) ? parsed : [];
  } catch (_) {
    return [];
  }
}

function setStoredArray(key, value) {
  localStorage.setItem(key, JSON.stringify(value));
}

function getStoredObject(key) {
  try {
    const raw = localStorage.getItem(key);
    const parsed = raw ? parseJsonOrNull(raw) : {};
    return parsed && typeof parsed === "object" ? parsed : {};
  } catch (_) {
    return {};
  }
}

function setStoredObject(key, value) {
  localStorage.setItem(key, JSON.stringify(value));
}

function trackVisitor() {
  const marker = "sevelVisitTracked";
  if (sessionStorage.getItem(marker)) return;
  const metrics = getStoredObject(SEVEL_STORAGE.metrics);
  metrics.visits = Number(metrics.visits || 0) + 1;
  setStoredObject(SEVEL_STORAGE.metrics, metrics);
  sessionStorage.setItem(marker, "1");
}

function saveContactSubmission(payload) {
  const submissions = getStoredArray(SEVEL_STORAGE.submissions);
  submissions.push({
    id: "s_" + Date.now() + "_" + Math.random().toString(36).slice(2, 7),
    name: payload.name || "",
    email: payload.email || "",
    phone: payload.phone || "",
    subject: payload.subject || "",
    message: payload.message || "",
    status: "Pending",
    createdAt: new Date().toISOString(),
  });
  setStoredArray(SEVEL_STORAGE.submissions, submissions);
}

function saveNewsletterSubscriber(email) {
  const clean = String(email || "").trim().toLowerCase();
  if (!clean) return { ok: false, reason: "empty" };

  const subscribers = getStoredArray(SEVEL_STORAGE.newsletter);
  if (subscribers.some((s) => String(s.email || "").toLowerCase() === clean)) {
    return { ok: false, reason: "exists" };
  }

  subscribers.push({
    id: "n_" + Date.now() + "_" + Math.random().toString(36).slice(2, 7),
    email: clean,
    createdAt: new Date().toISOString(),
  });
  setStoredArray(SEVEL_STORAGE.newsletter, subscribers);
  return { ok: true };
}

function initializeNewsletterForms() {
  document.querySelectorAll(".newsletter").forEach((box) => {
    if (box.dataset.boundNewsletter === "1") return;
    box.dataset.boundNewsletter = "1";

    const input = box.querySelector('input[type="email"]');
    const button = box.querySelector("button");
    if (!input || !button) return;

    const submitNewsletter = () => {
      const email = input.value.trim();
      const isValid = /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email);
      if (!isValid) {
        alert("Please enter a valid email.");
        return;
      }

      const result = saveNewsletterSubscriber(email);
      if (result.ok) {
        alert("Subscribed successfully.");
      } else if (result.reason === "exists") {
        alert("This email is already subscribed.");
      } else {
        alert("Unable to subscribe right now.");
      }

      input.value = "";
    };

    button.addEventListener("click", function (e) {
      e.preventDefault();
      submitNewsletter();
    });

    input.addEventListener("keydown", function (e) {
      if (e.key === "Enter") {
        e.preventDefault();
        submitNewsletter();
      }
    });
  });
}

