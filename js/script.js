function stripFlagSuffix(value) {
  return value.replace(/\s*(?:[\u{1F1E6}-\u{1F1FF}]{2}|[A-Z]{2})$/u, "").trim();
}

function applyGalleryFlagIcons(lang) {
  const locationEls = document.querySelectorAll('[data-i18n^="gallery-location"]');
  locationEls.forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const country = galleryLocationCountryByKey[key];
    const twemojiCode = galleryFlagSvgByCountry[country];
    if (!country || !twemojiCode) return;

    const translated = translations[lang] && translations[lang][key] ? translations[lang][key] : el.textContent || "";
    const baseLabel = stripFlagSuffix(translated);
    const flagUrl = `https://cdn.jsdelivr.net/gh/twitter/twemoji@14.0.2/assets/svg/${twemojiCode}.svg`;

    el.innerHTML = `${baseLabel} <img class="flag-emoji" src="${flagUrl}" alt="${country.toUpperCase()} flag" loading="lazy" decoding="async">`;
  });
}

function applyTranslations(lang) {
  if (typeof translations === "undefined" || !translations[lang]) return;

  const elements = document.querySelectorAll("[data-i18n]");
  elements.forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang][key]) {
      const value = translations[lang][key];
      if ((el.tagName === "INPUT" || el.tagName === "TEXTAREA") && el.hasAttribute("placeholder")) {
        el.placeholder = value;
      } else if (el.tagName === "INPUT" && (el.type === "submit" || el.type === "button")) {
        el.value = value;
      } else {
        el.innerHTML = value;
      }
    }
  });

  applyGalleryFlagIcons(lang);
}

