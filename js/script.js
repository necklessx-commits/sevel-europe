function initializeUserMenu() {
  const userMenuBtn = document.getElementById("userMenuBtn");
  if (!userMenuBtn) return;

  const user = localStorage.getItem("publicUser");
  const adminUser = localStorage.getItem("adminUser");
  const isAdmin =
    localStorage.getItem("adminToken") && localStorage.getItem("adminRole") === "admin";

  if (isAdmin && adminUser) {
    userMenuBtn.textContent = "Dashboard";
    userMenuBtn.onclick = openProfileModal;
    return;
  }

  if (user) {
    const userData = parseJsonOrNull(user);
    if (!userData) {
      userMenuBtn.textContent = "Login";
      userMenuBtn.onclick = openLoginModal;
      return;
    }
    userMenuBtn.textContent = userData.name;
    userMenuBtn.onclick = openProfileModal;
    return;
  }

  userMenuBtn.textContent = "Login";
  userMenuBtn.onclick = openLoginModal;
}

function initializeRoleNavigation() {
  const nav = document.querySelector("header nav");
  if (!nav) return;

  const isAdmin =
    localStorage.getItem("adminToken") && localStorage.getItem("adminRole") === "admin";
  const existingAdminLink = nav.querySelector('[data-nav-role="admin"]');

  if (!isAdmin) {
    if (existingAdminLink) existingAdminLink.remove();
    return;
  }

  if (existingAdminLink) return;

  const adminLink = document.createElement("a");
  adminLink.href = "admin-dashboard.html";
  adminLink.textContent = "Admin";
  adminLink.setAttribute("data-nav-role", "admin");
  const contactLink =
    nav.querySelector('a[href="contact.html"]') || nav.querySelector('a[data-i18n="nav-contact"]');
  if (contactLink && contactLink.nextSibling) {
    nav.insertBefore(adminLink, contactLink.nextSibling);
  } else if (contactLink) {
    nav.appendChild(adminLink);
  } else {
    nav.appendChild(adminLink);
  }
}

